//:输出3 ~ 100 以质数
//规则:只有1和它自身能整除尽   % == 0
//3 5 7 11[2 3 4 5 6 7 8 9 10] 
//15:12--15:17

//1:创建外层循环 3-100  数值1
for(var i=3;i<=100;i++){      //i=5
 //2:创建内层循环 2-i-1  数值2
 for(var j=2;j<i;j++){        //j=5
  //3:如果数值2可以除尽数值1
  if(i%j==0){
   //4:退出循环 不是质数
   break;
  }
  //5:循环外
 }//for j end
 //6:如果数值1==数值2  是质数
 if(i==j){
   console.log("是质数:"+i);
 }
}//for i end